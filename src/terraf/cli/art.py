_LAND = set("MH")   # caracteres de masa terrestre вҶ’ verde brillante


def _build_earth() -> Text:
    """Colorea el ASCII art carГЎcter a carГЎcter."""
    t = Text(no_wrap=True)
    for ch in _EARTH_RAW:
        if ch == "\n":
            t.append("\n")
        elif ch == " ":
            t.append(" ")
        elif ch in _LAND:
            t.append(ch, style="bright_green")
        else:
            t.append(ch, style="cyan")
    return t


